public class Operators {
    public static void main(String[] args) {

//      Arithmetic Operators
/*
        +  Add
        -  Subtract
        *  Multiply
        /  Divide
        %  Modulo
        ++ Increase
        -- Decrease
*/

//      Examples
        int num1 = 27;
        int num2 = 5;

        System.out.println(num1 + num2);

        System.out.println(num1 - num2);

        System.out.println(num1 * num2);

        System.out.println(num1 / num2);

        System.out.println(num1 % num2);

        num1 ++;
        System.out.println(num1);
        num2 ++;
        System.out.println(num2);

        num1 --;
        System.out.println(num1);
        num2 --;
        System.out.println(num2);


//      Assignment Operators
/*
         =
        += Plus Assignment
        -= Minus Assignment
        *= Multiplication Assignment
        %= Modulo Assigment
        /= Division Assignment
*/

//      Examples
        int num3 = 10;
        System.out.println(num3);

        System.out.println(num3 + 5);


//        Comparison Operators
/*
        ==
        !=
         >
         <
        >=
        <=
 */

//      Examples
        System.out.println(num1 == num2);
        System.out.println(num1 == num1);


//        Logical Operators
/*
        && => AND   = All values need to be true before the output is true.

        || => OR  = Exactly one, or more of the values has to be true before the output is true.

         |  => NOT
 */
        //      Examples


    }
}
